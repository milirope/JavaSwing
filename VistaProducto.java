    private void jTablePersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePersonasMouseClicked
        // TODO add your handling code here:
        int row = this.jTablePersonas.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) this.jTablePersonas.getModel();
        
        Object cedula = model.getValueAt(row, 0);
        Object nombre = model.getValueAt(row, 1);
        Object apellido = model.getValueAt(row, 2);
        
        this.skuInput.setText(cedula.toString());
        this.nombreInput.setText(nombre.toString());
        this.precioInput.setText(apellido.toString());
        
        this.validateButton();
        
    }//GEN-LAST:event_jTablePersonasMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        
        String sku = this.skuInput.getText();
        String nombre = this.nombreInput.getText();
        String precio = this.precioInput.getText();
        String oferta = this.ofertaInput.getText();
        String stock = this.stockInput.getText();
        String peso = this.pesoInput.getText();
        
        if(peso.length() == 0){
            peso = "0";
        }
        
        if(oferta.length() == 0){
            oferta = "0";
        }
        
        System.out.println(oferta);
        System.out.println(peso);
        
        this.addProducto(
            sku, 
            nombre, 
            Double.parseDouble(precio),
            Double.parseDouble(oferta),
            Integer.parseInt(stock),
            Double.parseDouble(peso)
        );
        this.getProductos();
    }//GEN-LAST:event_addButtonActionPerformed

